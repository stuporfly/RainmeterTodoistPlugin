namespace PluginTodoist.TodoistModel
{
    /// <summary>
    /// Represents date formats.
    /// </summary>
    public enum DateFormat : byte
    {
        /// <summary>
        /// DD-MM-YYYY date format.
        /// </summary>
        DdMmYyyy = 0,

        /// <summary>
        /// MM-DD-YYYY date format.
        /// </summary>
        MmDdYyyy = 1
    }
}
